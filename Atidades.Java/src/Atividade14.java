
public class Atividade14 {
    public static void main(String[] args) {
        int a = 20, b = 10, soma = 0, sub = 0, produto = 0;
        double quociente = 0, resto = 0;

        soma = a + b;
        sub = a - b;
        produto = a * b;
        quociente = a / b;
        resto = a % b;

        System.out.println("A soma de 20 e 10 é: " + soma + "\n" +
                "A subtração de 20 e 10 é: " + sub + "\n" +
                "O produto de 20 e 10 é: " + produto + "\n" +
                "O quociente de 20 e 10 é: " + quociente + "\n" +
                "E o resto é: " + resto);

    }
}
